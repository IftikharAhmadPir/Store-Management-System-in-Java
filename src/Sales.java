    private void btnsellActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsellActionPerformed
        String recption= txtrecptionnumber.getText();
        String id= (String) CMBboxproduct.getSelectedItem();
        String name= txtname.getText();
        String price=txtproductprice.getText();
        String stock=txtStock.getText();
        String date = txtdate.getText();
        String quantity =txtproductquantity.getText();
        String customername= txtcustomer.getText();
        String sellername= txtsellername.getText();
        int stock1= Integer.parseInt(txtStock.getText());
        int quant= Integer.parseInt(txtproductquantity.getText());
        if(quant<=stock1) {
            int newstock= stock1-quant;
            String query="Update Product set Stock='"+newstock+"' where ProductID='"+id+"'";
            boolean r= obj.UDI(query);
            if(r=true) {
                String sql= "INSERT INTO Sales VALUES('"+recption+"','"+id+"','"+name+"','"+price+"','"+stock+"','"+date+"','"+quantity+"','"+customername+"','"+sellername+"')";
                boolean rs= obj.UDI(sql);
            } else {
                JOptionPane.showMessageDialog(null,"Wrong Data");
            }
        }
        txtrecptionnumber.setText("");
        txtdate.setText("");
        txtproductquantity.setText("");
        txtcustomer.setText("");
        txtsellername.setText("");

    }//GEN-LAST:event_btnsellActionPerformed

